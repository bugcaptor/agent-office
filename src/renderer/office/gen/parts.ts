export function EMPTY16(): PixelRows {
  return Array.from({ length: 16 }, () => '................');
}
function overlayRows(rows: Record<number, string>): PixelRows {
  const out = EMPTY16().slice();
  for (const [y, s] of Object.entries(rows)) out[+y] = (s + '................').slice(0, 16);
  return out;
}

